namespace Fluxera.Queries.Options
{
	/// <summary>
	///		A class holding constants with the query parameter names.
	/// </summary>
	public static class ParameterNames
	{
		/// <summary>
		///		The $filter parameter name.
		/// </summary>
		public const string Filter = "$filter";

		/// <summary>
		///		The $orderby parameter name.
		/// </summary>
		public const string OrderBy = "$orderby";

		/// <summary>
		///		The $skip parameter name.
		/// </summary>
		public const string Skip = "$skip";

		/// <summary>
		///		The $skiptoken parameter name.
		/// </summary>
		public const string SkipToken = "$skiptoken";

		/// <summary>
		///		The $top parameter name.
		/// </summary>
		public const string Top = "$top";

		/// <summary>
		///		The $count parameter name.
		/// </summary>
		public const string Count = "$count";

		/// <summary>
		///		The $select parameter name.
		/// </summary>
		public const string Select = "$select";

		/// <summary>
		///		The $search parameter name.
		/// </summary>
		public const string Search = "$search";
	}
}
